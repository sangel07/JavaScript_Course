alert(`Redondeo normal: ${Math.round(5.63)}`); //Redondea al entero mas cercano
alert(`Redondeo hacia el piso: ${Math.floor(5.63)}`); //Redondea al entero más cercano hacia abajo
alert(`Redondea hacia el techo: ${Math.ceil(5.63)}`); //Redondea al entero más cercano hacia arriba